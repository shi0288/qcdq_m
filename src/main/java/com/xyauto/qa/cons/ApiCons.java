package com.xyauto.qa.cons;

import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;

/**
 * Created by shiqm on 2017/3/9.
 */

@Component
@ConfigurationProperties(prefix = "xyauto.qa")
public class ApiCons {

    private String app_id;
    private String sign;
    private String server_name;
    private String server_car_name;
    private String server_news_name;
    private String server_points_name;
    private String server_search_name;

    public String getApp_id() {
        return app_id;
    }

    public void setApp_id(String app_id) {
        this.app_id = app_id;
    }

    public String getSign() {
        return sign;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }

    public String getServer_name() {
        return server_name;
    }

    public void setServer_name(String server_name) {
        this.server_name = server_name;
    }

    public String getServer_car_name() {
        return server_car_name;
    }

    public void setServer_car_name(String server_car_name) {
        this.server_car_name = server_car_name;
    }

    public String getServer_news_name() {
        return server_news_name;
    }

    public void setServer_news_name(String server_news_name) {
        this.server_news_name = server_news_name;
    }

    public String getServer_points_name() {
        return server_points_name;
    }

    public void setServer_points_name(String server_points_name) {
        this.server_points_name = server_points_name;
    }

    public String getServer_search_name() {
        return server_search_name;
    }

    public void setServer_search_name(String server_search_name) {
        this.server_search_name = server_search_name;
    }
}
