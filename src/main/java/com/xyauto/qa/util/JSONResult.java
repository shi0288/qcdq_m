package com.xyauto.qa.util;

import com.xyauto.qa.cons.CommonCons;

/**
 * Created by shiqm on 2017/3/17.
 */
public class JSONResult {

    private boolean done;

    private String htmlNode;

    private int limit = CommonCons.Page_Flag.PAGE_LIMIT;

    private int count;

    private int hasMore;

    private String  nextMax;


    @Override
    public String toString() {
        return "{" +
                "done=" + done +
                ", htmlNode='" + htmlNode + '\'' +
                '}';
    }

    public boolean isDone() {
        return done;
    }

    public void setDone(boolean done) {
        this.done = done;
    }

    public String getHtmlNode() {
        return htmlNode;
    }

    public void setHtmlNode(String htmlNode) {
        this.htmlNode = htmlNode;
    }


    public int getLimit() {
        return limit;
    }


    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getHasMore() {
        return hasMore;
    }

    public void setHasMore(int hasMore) {
        this.hasMore = hasMore;
    }

    public String getNextMax() {
        return nextMax;
    }

    public void setNextMax(String nextMax) {
        this.nextMax = nextMax;
    }
}
